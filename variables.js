// var is used to declare a variable in JavaScript. It can be reassigned and redeclared within its scope. However, it is generally recommended to use let or const instead of var for better scoping and to avoid potential issues with hoisting.

// In JavaScript, there are three ways to declare variables: var, let, and const. Each has its own characteristics and use cases.


let age=25;
console.log(age);

// let is used to declare a block-scoped variable in JavaScript. It can be reassigned but cannot be redeclared within the same scope. It is generally preferred over var for better scoping and to avoid potential issues with hoisting.


const  place="hyderabad";
console.log(place);

// const is used to declare a block-scoped variable in JavaScript that cannot be reassigned or redeclared. It is used for values that should remain constant throughout the program. However, if the value is an object or an array, its properties or elements can still be modified.